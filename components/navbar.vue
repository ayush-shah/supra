<script>
export default {
  name: "Navbar",
  mounted: () => {
    let nav = document.getElementsByTagName("nav")[0];
    let quickInfo = document.getElementsByClassName("quick-info")[0];

    window.onscroll = () => {
      if (
        document.body.scrollTop > nav.scrollHeight ||
        (document.documentElement.scrollTop > nav.scrollHeight &&
          nav.classList.contains("fixedNav"))
      ) {
      } else if (
        document.body.scrollTop > nav.scrollHeight ||
        document.documentElement.scrollTop > nav.scrollHeight
      ) {
        nav.classList.add("fixedNav");
        quickInfo.classList.add("fixedNav");
      } else {
        nav.classList.remove("fixedNav");
        quickInfo.classList.remove("fixedNav");
      }
    };
  },
};
